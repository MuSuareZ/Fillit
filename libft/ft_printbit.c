#include "libft.h"

void	ft_printbit(int n)
{
	int i;

	i = 128;
	while (i > 0)
	{
		if (n >= i)
		{
			write(1, "1", 1);
			n = n % i;
		}
		else
			write(1, "0", 1);
		i /= 2;
	}
}
