#include "libft.h"

int	ft_isxdigit(int c)
{
	unsigned char b;

	b = (unsigned char)c;
	if ((b >= 48 && b < 58) ||
	(b >= 'A' && b <= 'F') || (b >= 'a' && b <= 'f'))
		return (1);
	return (0);
}
