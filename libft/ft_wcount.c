#include "libft.h"

int		ft_wcount(char const *s, char c)
{
	int i;
	int j;

	i = -1;
	j = 0;
	while (s[++i])
	{
		if ((i == 0 && s[i] != c) || (s[i] != c && s[i + 1] == c))
			j++;
	}
	return (j);
}
